import Foundation
import UIKit

extension UIColor {
    
    class func placeHolderColor() -> UIColor {
        return UIColor(red: 95/255.0, green: 96/255.0, blue: 99/255.0, alpha: 1.0)
    }
    
    class func borderColor() -> UIColor {
        return UIColor(red: 239/255.0, green: 239/255.0, blue: 239/255.0, alpha: 1.0)
    }
}
